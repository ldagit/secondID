public class Calculator {
    public Calculator() {

    }

    public double sum(int num1, int num2) {
        return (num1 + num2);
    }

    public double difference(int num1, int num2) {
        return (num1 - num2);
    }

    public double product(int num1, int num2) {
        return (num1 * num2);
    }

    public double quotient(int num1, int num2) {
        return (num1/num2);
    }
}
